async function findCodeByClaude(schoolName: string): Promise<number | null> {
  const res = await claudeCall(
    `以下の中学校リスト（形式: コード番号: 学校名）から「${schoolName}」に一致する学校のコード番号を返してください。

マッチングルール:
- 完全一致を最優先
- 地名（所沢・日本橋・千葉など）が含まれる場合は必ずその地名も一致させる
- 「中学校」「中等部」「中等教育学校」などの違いは無視してよい
- 同じ法人名の学校が複数ある場合は地名・コース名で区別する
- 数字のみ返す。見つからない場合は 0 を返す。

検索: 「${schoolName}」

リスト:
${SCHOOL_LIST}`,
    16
  );
  const num = parseInt(res.trim());
  return isNaN(num) || num === 0 ? null : num;
}

async function fetchDeviation(code: number): Promise<{ round: string; a80: number; c50: number }[]> {
  const res = await fetch(`${BASE}/?code=${code}`, { headers: BROWSER_HEADERS });
  if (!res.ok) throw new Error(`detail fetch: ${res.status}`);
  const html = await res.text();

  const text = html
    .replace(/<script[\s\S]*?<\/script>/gi, '')
    .replace(/<style[\s\S]*?<\/style>/gi, '')
    .replace(/<[^>]+>/g, ' ')
    .replace(/&[a-z]+;/g, ' ')
    .replace(/\s+/g, ' ')
    .substring(0, 4000);

  const json = await claudeCall(
    `以下は四谷大塚の中学校詳細ページのテキストです。
「Aライン80偏差値」（合格率80%）と「Cライン50偏差値」（合格率50%）を入試回ごとに抽出してください。

出力形式（JSONのみ、他の説明不要）：
[{"round":"第1回（2/1）","a80":66,"c50":62}]

テキスト:
${text}`,
    512
  );

  const match = json.match(/\[[\s\S]*?\]/);
  if (!match) throw new Error('deviation JSON not found');
  return JSON.parse(match[0]);
}

async function claudeCall(prompt: string, maxTokens: number): Promise<string> {
  const res = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'x-api-key': ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01',
      'content-type': 'application/json',
    },
    body: JSON.stringify({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: maxTokens,
      messages: [{ role: 'user', content: prompt }],
    }),
  });
  if (!res.ok) throw new Error(`Claude API: ${res.status}`);
  const data = await res.json();
  return data.content[0].text;
}

serve(async (req) => {
  if (req.method === 'OPTIONS') return new Response('ok', { headers: cors });

  try {
    const { schoolName } = await req.json();

    const code = await findCodeByClaude(schoolName);
    if (!code) {
      return new Response(
        JSON.stringify({ error: `「${schoolName}」は四谷大塚の掲載校リストに見つかりませんでした` }),
        { status: 404, headers: { ...cors, 'Content-Type': 'application/json' } }
      );
    }

    const entries = await fetchDeviation(code);
    if (!entries.length) {
      return new Response(
        JSON.stringify({ error: '偏差値データを取得できませんでした' }),
        { status: 500, headers: { ...cors, 'Content-Type': 'application/json' } }
      );
    }

    return new Response(
      JSON.stringify({ code, entries }),
      { headers: { ...cors, 'Content-Type': 'application/json' } }
    );
  } catch (err) {
    return new Response(
      JSON.stringify({ error: String(err) }),
      { status: 500, headers: { ...cors, 'Content-Type': 'application/json' } }
    );
  }
});
